package kimia.data;

public class H {
    private int id;
    private double cis;
    private double trans;
    private double gem;
    private String r;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public double getCis() {
        return cis;
    }

    public void setCis(double cis) {
        this.cis = cis;
    }

    public double getTrans() {
        return trans;
    }

    public void setTrans(double trans) {
        this.trans = trans;
    }

    public double getGem() {
        return gem;
    }

    public void setGem(double gem) {
        this.gem = gem;
    }

    public String getR() {
        return r;
    }

    public void setR(String r) {
        this.r = r;
    }
}
